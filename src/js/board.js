class BoardLib {
  static getBoard = () => {
    // return new Table(InitialBoardGame);
    var to_return = Table.fromObject(InitialBoardGame);

    return to_return;
  }

  static cloneTable = (board) => {
    return Table.fromObject(Table.toObject(board));
  }

  static exportToJson = (board) => {
    return JSON.stringify(Table.toObject(board), null, '\t');
  }

  static loadTable = (data) => {
    return Table.fromObject(JSON.parse(data));
  }

  static uuidToComponent = (table, uuid) => {
    let toReturn = BoardLib.uuidToComponentWithParent(table, uuid);
    if(toReturn)
      return toReturn.component;
    return null;
  }

  static uuidToComponentWithParent = (table, uuid) => {
    // Returns the component with the UUID on a table

    // Searches all components on the board
    for(let i = 0; i < table.components.length; i++) {
      if(table.components[i].uuid === uuid) {
        return {
          component: table.components[i],
          parent: table.components,
          index: i
        }
      };
    }

    // Searches all stacks on the board
    for(let stackIndex = 0; stackIndex < table.stacks.length; stackIndex++) {
      let stack = table.stacks[stackIndex];
      if(stack.uuid === uuid) {
        return {
          component: stack,
          parent: table.stacks,
          index: stackIndex,
        };
      }

      // Search for all members of this stack
      for(let i = 0; i < stack.members.length; i++) {
        let component = stack.members[i];
        if(component.uuid === uuid) {
          return {
            component: component,
            parent: stack.members,
            index: i,
          };
        }
      }
    }
    return null;
  }
}

export default BoardLib;
